#include "Zako.h"

#include <Enemy/Movement/HomingMove.h>

/////////////////////////////////////////////////////////////////////////////////////////
//			ctor
/////////////////////////////////////////////////////////////////////////////////////////
Zako::Zako()
	: BaseEnemy("debugCube.obj", "Zako") {
}

/////////////////////////////////////////////////////////////////////////////////////////
//			初期化
/////////////////////////////////////////////////////////////////////////////////////////
void Zako::Initialize() {
	EnemyStats stats;
	stats.maxHp = 5;
	stats.moveSpeed = 1.5f;
	stats.contactDamage = 1;
	stats.attackRange = 1.2f;
	stats.detectionRange = 6.0f;
	SetStats(stats);

	SetMovement(std::make_unique<HomingMove>());
	BaseEnemy::Initialize();
}