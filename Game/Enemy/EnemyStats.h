#pragma once

/*-----------------------------------------------------------------------------------------
 * EnemyStats
 * - 敵のパラメータだけをまとめた構造体
 *---------------------------------------------------------------------------------------*/
struct EnemyStats {
	int   maxHp = 10;    // 最大HP
	float moveSpeed = 2.0f;  // 移動速度

	// --- ダメージ関連 ---
	int   contactDamage = 1;     // 体に触れている間に与えるダメージ
	int   attackDamage = 2;     // 攻撃アクションが当たったときのダメージ
	float attackRange = 1.5f;  // この距離まで近づくと攻撃に移る
	float attackInterval = 2.0f;  // 攻撃の間隔

	// --- 索敵 ---
	float detectionRange = 8.0f;  // プレイヤーを感知して動き出す距離
};