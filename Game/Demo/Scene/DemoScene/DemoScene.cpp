DemoScene::DemoScene(){
	// シーン名を設定
	SetSceneName("DemoScene");

}

/////////////////////////////////////////////////////////////////////////////////////////
//	アセットのロード
/////////////////////////////////////////////////////////////////////////////////////////
void DemoScene::LoadAssets() {}

/////////////////////////////////////////////////////////////////////////////////////////
//	初期化処理
/////////////////////////////////////////////////////////////////////////////////////////
void DemoScene::Initialize(){
	sceneContext_->Initialize();

	sceneContext_->SetSceneName("DemoScene");

	BaseScene::Initialize();

	std::string scenePath = "Resources/Assets/Scenes/DemoScene.scene";
	SceneSerializer::Load(*sceneContext_, scenePath);
	sceneContext_->SetScenePath(scenePath);

	LoadAssets();

	player_ = sceneContext_->FindObjectByName<Player>("Player");
	if (player_) {
		player_->Initialize();
	}
	slime_ = sceneContext_->FindObjectByName<Slime>("Slime");
	if (slime_) {
		slime_->Initialize();
		if (!player_) return;
		slime_->SetTarget(player_.get());
	}

}

